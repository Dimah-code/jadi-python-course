import math

print("Pi:", math.pi)
print("Euler's number (e):", math.e)

num = 16
print("Square root of", num, "is", math.sqrt(num))
print("2 raised to the power 5 is", math.pow(2, 5))

angle = math.radians(45)  # convert degrees to radians
print("sin(45°):", math.sin(angle))
print("cos(45°):", math.cos(angle))

value = 5.67
print("Floor:", math.floor(value))
print("Ceil:", math.ceil(value))

print("5! =", math.factorial(5))
print("Combinations of 5 choose 2:", math.comb(5, 2))
