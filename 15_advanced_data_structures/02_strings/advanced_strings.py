s = "Dimah is cool"
print("The string:", s)
# convert to uppercase all letters
print("uppercase string:", s.upper())

# convert the first letter to upper
print("capitalize string:", s.capitalize())

# convert each word's first letter to upper
print("make the string like a title: ", s.title())

# convert whole string to lower
us = "DIMAH Is HERE"
print("make the string to lowercase:", us.lower())

# count the letter in string
print("return the count of `i`: ", s.count("i"))

# find the letter in a string -> return index of the letter
print("index of `m` in string: ", s.find("m"))

print("index of `z` in string:", s.find("z"))

# is this string printable?
print("is this string printable?", s.isprintable())
