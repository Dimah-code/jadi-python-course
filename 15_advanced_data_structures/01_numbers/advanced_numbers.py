# int numbers
print("INT NUMBERS")

int_number = 4
print("Integer number:", int_number)

# float numbers
print("FLOAT NUMBERS")

float_number = 4.5
print("Float numbers:", float_number)

# hexdecimal numbers
print("HEXDECIMAL NUMBBERS")

hex = hex(12)
print("Hexdecimal:", hex)

# oct numbers
print("OCTAL NUMBERS")

oct = oct(12)
print("octal:", oct)

# binary numbers
print("BINARY NUMBERS")

bin = bin(12)
print("binary numbers: ", bin)

print("loop with binary range: ")
for i in range(0b1100):
    print(i)  # 0, 1, 2, 3, ... 10, 11

# round numbers

print("ROUND NUMBERS")

print("round 2.5", round(2.5))
print("round 1.2", round(1.2))
print("round 2.7", round(2.7))
